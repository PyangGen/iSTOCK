  dateForwardBtn?.addEventListener("click", function (e) {
    e.stopPropagation();

    const today = getTodayPH();

    if (state.type === "preset") {
      movePresetAnchor(1);
      renderDateNav();
      syncInputsFromState();
      return;
    }

    const start = fromInputValue(state.customStart);
    const end = fromInputValue(state.customEnd);
    if (!start || !end) return;

    const diffDays = Math.round((end - start) / 86400000);
    let nextStart = addDays(start, diffDays + 1);
    let nextEnd = addDays(end, diffDays + 1);

    if (nextEnd > today) {
      nextEnd = today;
      nextStart = addDays(today, -diffDays);
    }

    state.customStart = toInputValue(nextStart);
    state.customEnd = toInputValue(nextEnd);
    renderDateNav();
    syncInputsFromState();
  });

  if (reportStartDate) {
    flatpickr(reportStartDate, {
      dateFormat: "Y-m-d",
      defaultDate: "today",
      maxDate: "today"
    });
  }

  if (reportEndDate) {
    flatpickr(reportEndDate, {
      dateFormat: "Y-m-d",
      defaultDate: "today",
      maxDate: "today"
    });
  }

  renderDateNav();
  syncInputsFromState();

});
