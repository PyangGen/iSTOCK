    if (!start || !end) return;

    const diffDays = Math.round((end - start) / 86400000);
    const nextStart = addDays(start, -(diffDays + 1));
    const nextEnd = addDays(end, -(diffDays + 1));

    state.customStart = toInputValue(nextStart);
    state.customEnd = toInputValue(nextEnd);
    renderDateNav();
    syncInputsFromState();
