function getPresetRange(preset, anchor = getTodayPH()) {
  const today = getTodayPH();
  const base = cloneDate(anchor);
  const actualYesterday = addDays(today, -1);

  switch (preset) {
    case "today": {
      if (sameDate(base, today)) {
        return {
          start: base,
          end: base,
          label: `Today : ${formatDayShort(base)}`,
          showForward: false
        };
      }

      if (sameDate(base, actualYesterday)) {
        return {
          start: base,
          end: base,
          label: `Yesterday: ${formatDayShort(base)}`,
          showForward: true
        };
      }

      return {
        start: base,
        end: base,
        label: formatDayFull(base),
        showForward: true
      };
    }

    case "yesterday": {
      if (sameDate(base, actualYesterday)) {
        return {
          start: base,
          end: base,
          label: `Yesterday: ${formatDayShort(base)}`,
          showForward: true
        };
      }

      if (sameDate(base, today)) {
        return {
          start: base,
          end: base,
          label: `Today : ${formatDayShort(base)}`,
          showForward: false
        };
      }

      return {
        start: base,
        end: base,
        label: formatDayFull(base),
        showForward: true
      };
    }

    case "this_week": {
      const start = startOfWeek(base);
      let end = endOfWeek(base);

      if (end > today) end = today;

      return {
        start,
        end,
        label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
        showForward: !sameDate(end, today)
      };
    }

    case "last_week": {
      const currentWeekStart = startOfWeek(today);
      const currentWeekEnd = today;
      const previousWeekStart = addDays(currentWeekStart, -7);
      const previousWeekEnd = addDays(currentWeekStart, -1);

      const start = startOfWeek(base);
      const end = endOfWeek(base);

      if (sameDate(start, previousWeekStart) && sameDate(end, previousWeekEnd)) {
        return {
          start,
          end,
          label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
          showForward: true
        };
      }

      if (sameDate(start, currentWeekStart)) {
        return {
          start: currentWeekStart,
          end: currentWeekEnd,
          label: `${formatDayFull(currentWeekStart)} - ${formatDayFull(currentWeekEnd)}`,
          showForward: false
        };
      }

      return {
        start,
        end,
        label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
        showForward: true
      };
    }

    case "this_month": {
      const start = startOfMonth(base);
      let end = endOfMonth(base);

      if (
        base.getFullYear() === today.getFullYear() &&
        base.getMonth() === today.getMonth()
      ) {
        end = today;
      }

      return {
        start,
        end,
        label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
        showForward: !sameDate(end, today)
      };
    }

    case "last_month": {
      const currentMonthStart = startOfMonth(today);
      const currentMonthEnd = today;
      const previousMonthBase = addMonths(today, -1);
      const previousMonthStart = startOfMonth(previousMonthBase);
      const previousMonthEnd = endOfMonth(previousMonthBase);

      const start = startOfMonth(base);
      const end = endOfMonth(base);

      if (sameDate(start, previousMonthStart) && sameDate(end, previousMonthEnd)) {
        return {
          start,
          end,
          label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
          showForward: true
        };
      }

      if (
        start.getFullYear() === currentMonthStart.getFullYear() &&
        start.getMonth() === currentMonthStart.getMonth()
      ) {
        return {
          start: currentMonthStart,
          end: currentMonthEnd,
          label: `${formatDayFull(currentMonthStart)} - ${formatDayFull(currentMonthEnd)}`,
          showForward: false
        };
      }

      return {
        start,
        end,
        label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
        showForward: true
      };
    }

    case "this_year": {
      const start = startOfYear(base);
      let end = endOfYear(base);

      if (base.getFullYear() === today.getFullYear()) {
        end = today;
      }

      return {
        start,
        end,
        label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
        showForward: !sameDate(end, today)
      };
    }

    case "last_year": {
      const currentYearStart = startOfYear(today);
      const currentYearEnd = today;
      const previousYear = today.getFullYear() - 1;

      const start = startOfYear(base);
      const end = endOfYear(base);

      if (start.getFullYear() === previousYear) {
        return {
          start,
          end,
          label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
          showForward: true
        };
      }

      if (start.getFullYear() === today.getFullYear()) {
        return {
          start: currentYearStart,
          end: currentYearEnd,
          label: `${formatDayFull(currentYearStart)} - ${formatDayFull(currentYearEnd)}`,
          showForward: false
        };
      }

      return {
        start,
        end,
        label: `${formatDayFull(start)} - ${formatDayFull(end)}`,
        showForward: true
      };
    }

    default:
      return {
        start: today,
        end: today,
        label: `Today : ${formatDayShort(today)}`,
        showForward: false
      };
  }
}

function movePresetAnchor(direction) {
  const today = getTodayPH();

  switch (state.preset) {
    case "today":
    case "yesterday":
      state.anchorDate = addDays(state.anchorDate, direction);
      if (state.anchorDate > today) state.anchorDate = today;
      break;

    case "this_week":
    case "last_week":
      state.anchorDate = addDays(state.anchorDate, direction * 7);
      if (state.anchorDate > today) state.anchorDate = today;
      break;

    case "this_month":
    case "last_month":
      state.anchorDate = addMonths(state.anchorDate, direction);
      if (
        state.anchorDate.getFullYear() > today.getFullYear() ||
        (state.anchorDate.getFullYear() === today.getFullYear() &&
         state.anchorDate.getMonth() > today.getMonth())
      ) {
        state.anchorDate = today;
      }
      break;

    case "this_year":
    case "last_year":
      state.anchorDate = addYears(state.anchorDate, direction);
      if (state.anchorDate.getFullYear() > today.getFullYear()) {
        state.anchorDate = today;
      }
      break;
  }
}
  function renderDateNav() {
    const today = getTodayPH();

    if (state.type === "preset") {
      const range = getPresetRange(state.preset, state.anchorDate);
      reportDateLabel.textContent = range.label;
      dateForwardBtn.classList.toggle("is-hidden", !range.showForward);
      return;
    }

    const start = fromInputValue(state.customStart);
    const end = fromInputValue(state.customEnd);

    if (start && end) {
      reportDateLabel.textContent = `${formatDayFull(start)} - ${formatDayFull(end)}`;
      dateForwardBtn.classList.toggle("is-hidden", sameDate(end, today));
    } else {
      reportDateLabel.textContent = `Today : ${formatDayShort(today)}`;
      dateForwardBtn.classList.add("is-hidden");
    }
  }

  function syncInputsFromState() {
    if (state.type === "preset") {
      const range = getPresetRange(state.preset, state.anchorDate);
      if (reportStartDate) reportStartDate.value = toInputValue(range.start);
      if (reportEndDate) reportEndDate.value = toInputValue(range.end);
      return;
    }

    const start = fromInputValue(state.customStart);
    const end = fromInputValue(state.customEnd);

    if (reportStartDate && start) reportStartDate.value = toInputValue(start);
    if (reportEndDate && end) reportEndDate.value = toInputValue(end);
  }

